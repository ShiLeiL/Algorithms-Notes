//题目：颠倒给定的 32 位无符号整数的二进制位。
//例：输入: 00000010100101000001111010011100
//   输出: 00111001011110000010100101000000

//这是别人的题解（个人的思路不值一提，所以做个记录，自己的位运算还得加强呀╮(╯_╰)╭）
//思路是轮着取n的最后一位，然后加到res中


public class Solution 
{
    public uint reverseBits(uint n) 
    {
        uint res = 0,a=1; 
        for(int i=0;i<32;i++) //因为是32位，所以循环32次
        { 
            res = (res << 1) | (n & a); //res先左移一位，再加上n的最后一位
            n >>= 1; //n右移一位
        }
        return res;
    }
}