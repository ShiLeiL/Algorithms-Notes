//题目：编写一个函数，输入是一个无符号整数，返回其二进制表达式中数字位数为 ‘1’ 的个数。
//例：输入: 00000000000000000000000000001011
//   输出: 3

//这是官方的题解（个人的思路不值一提）
//因为觉得太巧妙了，所以以此为记录
//思路是任意数字n与n-1做与运算，就会把最后一个1变为0


public class Solution 
{
    public int HammingWeight(uint n) 
    {
        int sum = 0;
        while (n != 0) 
        {
            sum++;
            n &= (n - 1);
        }
    return sum;
    }
}